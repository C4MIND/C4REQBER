def main() -> None:
    en_patch = PATCHES["en"]
    total_new = 0
    for lang in ["en", "ru", *FALLBACK_LANGS]:
        path = I18N / f"{lang}.json"
        data = json.loads(path.read_text(encoding="utf-8"))
        before = set(data.keys())
        patch = PATCHES.get(lang, en_patch)
        data.update(patch)
        new_count = len(set(patch.keys()) - before)
        total_new += new_count if lang == "en" else 0
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"v3 patched {lang}: +{len(patch)} keys ({new_count} new)")
    print(f"v3 total new keys (en baseline): {total_new}")


if __name__ == "__main__":
    main()
