"""
TURBO-CDI: Export Module
Export discoveries to various formats (PDF, Markdown, LaTeX, HTML)
"""

import json
from datetime import datetime
from typing import List, Optional, Dict, Any
from pathlib import Path


class ExportManager:
    """
    Export research results to multiple formats.
    """

    def __init__(self, output_dir: Optional[str] = None):
        if output_dir is None:
            output_dir = Path.home() / "TURBO-CDI" / "exports"

        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def export_discovery_markdown(
        self, discovery: Dict[str, Any], filename: Optional[str] = None
    ) -> str:
        """Export discovery as Markdown."""
        if filename is None:
            timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
            filename = f"discovery_{timestamp}.md"

        filepath = self.output_dir / filename

        content = f"""# Research Discovery

**Generated:** {discovery.get("created_at", datetime.now().isoformat())}  
**Domain:** {discovery.get("domain", "general")}  
**Confidence:** {discovery.get("confidence", 0.0):.2f}

---

## Problem Statement

{discovery.get("problem", "N/A")}

## Physical Contradiction

{discovery.get("contradiction", "N/A")}

## Generated Hypothesis

{discovery.get("hypothesis", "N/A")}

## C4 Navigation Path

```
{" → ".join(discovery.get("c4_path", []))}
```

**Steps taken:** {len(discovery.get("c4_path", []))}/6

## Falsifiability Criteria

"""

        for i, criterion in enumerate(discovery.get("falsifiability_criteria", []), 1):
            content += f"{i}. {criterion}\n"

        content += f"""

---

*Generated by TURBO-CDI v3.0*
"""

        with open(filepath, "w") as f:
            f.write(content)

        return str(filepath)

    def export_project_report(
        self,
        project: Dict[str, Any],
        discoveries: List[Dict],
        tasks: List[Dict],
        filename: Optional[str] = None,
    ) -> str:
        """Export full project report."""
        if filename is None:
            filename = f"project_{project.get('name', 'report').replace(' ', '_')}.md"

        filepath = self.output_dir / filename

        content = f"""# Research Project Report

## {project.get("name", "Untitled")}

**Status:** {project.get("status", "active")}  
**Domain:** {project.get("domain", "general")}  
**Created:** {project.get("created_at", "N/A")}

### Description

{project.get("description", "No description")}

### Objectives

"""

        for obj in project.get("objectives", []):
            content += f"- {obj}\n"

        content += f"""

### Hypotheses Generated ({len(discoveries)})

"""

        for i, disc in enumerate(discoveries, 1):
            content += f"#### {i}. {disc.get('hypothesis', 'N/A')[:100]}...\n\n"
            content += f"- **Confidence:** {disc.get('confidence', 0):.2f}\n"
            content += f"- **Status:** {disc.get('status', 'pending')}\n\n"

        content += f"""

### Tasks ({len(tasks)})

| Status | Count |
|--------|-------|
"""

        status_counts = {}
        for task in tasks:
            status = task.get("status", "unknown")
            status_counts[status] = status_counts.get(status, 0) + 1

        for status, count in sorted(status_counts.items()):
            content += f"| {status} | {count} |\n"

        content += f"""

---

*Report generated by TURBO-CDI v3.0*
"""

        with open(filepath, "w") as f:
            f.write(content)

        return str(filepath)

    def export_bibliography_latex(
        self, references: List[Dict], filename: str = "references.bib"
    ) -> str:
        """Export bibliography as LaTeX/BibTeX."""
        filepath = self.output_dir / filename

        with open(filepath, "w") as f:
            f.write("% Bibliography generated by TURBO-CDI\n\n")

            for ref in references:
                f.write(self._to_bibtex(ref))
                f.write("\n\n")

        return str(filepath)

    def _to_bibtex(self, ref: Dict) -> str:
        """Convert reference to BibTeX."""
        lines = [
            f"@{ref.get('entry_type', 'article')}{{{ref.get('cite_key', 'unknown')},"
        ]
        lines.append(f"  title = {{{ref.get('title', '')}}},")

        authors = ref.get("authors", [])
        if authors:
            lines.append(f"  author = {{{' and '.join(authors)}}},")

        lines.append(f"  year = {{{ref.get('year', 2024)}}},")

        if ref.get("journal"):
            lines.append(f"  journal = {{{ref['journal']}}},")

        if ref.get("doi"):
            lines.append(f"  doi = {{{ref['doi']}}},")

        lines.append("}")

        return "\n".join(lines)

