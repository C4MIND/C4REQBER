    def export_json(self, data: Dict[str, Any], filename: str = "export.json") -> str:
        """Export as JSON."""
        filepath = self.output_dir / filename

        with open(filepath, "w") as f:
            json.dump(data, f, indent=2, default=str)

        return str(filepath)
