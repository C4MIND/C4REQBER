// ActiveLanguage is the globally selected UI language.
var ActiveLanguage = LangEN

// T returns the translated string for the given key in the active language.
// Falls back to English if the key or language is missing.
func T(key string) string {
	if m, ok := translations[ActiveLanguage]; ok {
		if s, ok := m[key]; ok {
			return s
		}
	}
	if m, ok := translations[LangEN]; ok {
		if s, ok := m[key]; ok {
			return s
		}
	}
	return key
}

